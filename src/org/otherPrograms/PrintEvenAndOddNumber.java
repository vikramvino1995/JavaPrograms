package org.otherPrograms;

public class PrintEvenAndOddNumber {

	public static void main(String[] args) {
		
		for(int i=0;i<=100;i++) {
			
			if(i%2==0) {
				
				System.out.println("Even Number: "+i);
				
			}
			
			else if(i%2==1) {
				
				System.out.println("Odd Number: "+i);
				
			}
			
		}
		
	}
}
